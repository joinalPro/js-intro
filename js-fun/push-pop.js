var numbers = [55, 78, 43, 25, 98];

//use push to add element to an array as the last element array.
numbers.push(100)
console.log(numbers);

var friends = ['balam', 'kalam', 'salam']

friends.push('dilam')
console.log(friends);

// use pop to get rid of the last element array

numbers.pop();
console.log(numbers);

//use unfshift/shift to add/remove the element from array

numbers.shift()
console.log(numbers)
numbers.unshift(99)
console.log(numbers)